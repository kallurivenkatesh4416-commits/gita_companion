AppLanguageOption languageOptionFromCode(String code) {
  return supportedAppLanguages.firstWhere(
    (language) => language.code == code,
    orElse: () => supportedAppLanguages.first,
  );
}

const List<String> supportedGuidanceModes = <String>[
  'comfort',
  'clarity',
  'traditional',
];

String guidanceModeFromCode(String code) {
  final normalized = code.trim().toLowerCase();
  if (supportedGuidanceModes.contains(normalized)) {
    return normalized;
  }
  return supportedGuidanceModes.first;
}

class AppStrings {
  final String languageCode;

  const AppStrings(this.languageCode);

  String t(String key) {
    final languageMap = _values[languageCode] ?? _values['en']!;
    if (languageMap.containsKey(key)) {
      return languageMap[key]!;
    }
    final english = _values['en']![key];
    if (english != null) {
      return english;
    }
    return key;
  }

  String moodLabel(String mood) {
    final localized = _moodValues[languageCode] ?? _moodValues['en']!;
    return localized[mood] ?? mood;
  }

  String guidanceModeLabel(String mode) {
    switch (guidanceModeFromCode(mode)) {
      case 'comfort':
        return t('comfort_mode');
      case 'clarity':
        return t('clarity_mode');
      case 'traditional':
        return t('traditional_mode');
    }
    return t('comfort_mode');
  }
}

